#     bash bash/00_demarrer/premier_script.sh
#
# La 1re ligne (#!/usr/bin/env bash) est le "shebang" : elle dit "exécute-moi avec bash".
# Tout le reste des lignes qui commencent par # sont des COMMENTAIRES, ignorés par bash.

# echo affiche du texte. Mets-le entre guillemets doubles "...".
